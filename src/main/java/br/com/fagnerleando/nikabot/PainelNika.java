    private void atualizaCamposDadosCampanha(){

            lista_de_campanhas.forEach(item->{
                      
                String nomeCampanhaExtract = idCampanhaAtualizacaoAtual.substring(22,idCampanhaAtualizacaoAtual.length()).trim();
                                
                if(item.getNome().toLowerCase().replace(' ', '_').equals(nomeCampanhaExtract)){
                    
                    jTextField1.setText(item.getNome());
                    jFormattedTextField1.setText(item.getDtInicial());                
                    jFormattedTextField2.setText(item.getDtFinal());                                
                    jCheckBox1.setSelected(item.isStatusCriarPasta());
                    jCheckBox2.setSelected(item.isStatusCampanha());
                                        
                }
                     
            });
                   
    }

