    private void atualizaListaCampanha(){
    
       DefaultListModel listaModeloCampanhaList = new DefaultListModel();
       DefaultComboBoxModel listaModeloCampanhaCombo = new DefaultComboBoxModel();
        
        lista_de_campanhas.forEach(item -> {
            listaModeloCampanhaList.addElement( item.getIdCampanha() + " | " + item.getNome());
            listaModeloCampanhaCombo.addElement( item.getIdCampanha() + " | " + item.getNome());
        });
        
        jList3.setModel(listaModeloCampanhaList);
        jComboBox3.setModel(listaModeloCampanhaCombo);
        jComboBox2.setModel(listaModeloCampanhaCombo);            
    }
    
