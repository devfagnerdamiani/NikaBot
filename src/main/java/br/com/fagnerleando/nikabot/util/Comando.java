
package br.com.fagnerleando.nikabot.util;

public class Comando {
    
    private int programacaoCampanha;
    private String idComando;
    private String idCampanha;
    private String midia;
    private String posicao;
    private int desce;

    public int getProgramacaoCampanha() {
        return programacaoCampanha;
    }

    public void setProgramacaoCampanha(int programacaoCampanha) {
        this.programacaoCampanha = programacaoCampanha;
    }

    public String getIdComando() {
        return idComando;
    }

    public void setIdComando(String idComando) {
        this.idComando = idComando;
    }

    public String getIdCampanha() {
        return idCampanha;
    }

    public void setIdCampanha(String idCampanha) {
        this.idCampanha = idCampanha;
    }

    public String getMidia() {
        return midia;
    }

    public void setMidia(String midia) {
        this.midia = midia;
    }

    public String getPosicao() {
        return posicao;
    }

    public void setPosicao(String posicao) {
        this.posicao = posicao;
    }

    public int getDesce() {
        return desce;
    }

    public void setDesce(int desce) {
        this.desce = desce;
    }
    
}
